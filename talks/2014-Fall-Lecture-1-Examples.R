##--------------------------------------
## Example 2: Create AR(1) covariance matrix
## Hint: outer
##--------------------------------------

aa <- diag(10)
aa<- aa*10
rho <- 0.5

for(i in 1:10){
  for(j in 1:10){
    aa[i,j] <- rho^abs(i-j)
  }
}

a <- 1:10
b <- a

power <- outer(a, b, 
    function(x, y) return(abs(x-y)))

rho <- 0.5
rhoM <- rho^power

diag(rhoM) <- diag(rhoM)*10




